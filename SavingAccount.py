from .BankAccount import BankAccount


class SavingAccount(BankAccount):  #done
    def __init__(self, accountNumber, accountHolder, balance, interestRate, days):
        super().__init__(accountNumber, accountHolder, balance)
        self.interestRate = interestRate
        self.days=days


    def AddInterest(self):
        interestAmount = self.balance * self.interestRate * self.days/ 100 +self.balance

        return interestAmount
    
    
 






















































































































































































































































































 